/* ---------------------------------------------------------------
   ENQUEUE ASSETS
--------------------------------------------------------------- */
function ag_enqueue_assets() {
    // Google Fonts
    wp_enqueue_style(
        'ag-fonts',
        'https://fonts.googleapis.com/css2?family=Inter:wght@400;500;600;700;800&display=swap',
        [], null
    );

    // Main stylesheet
    wp_enqueue_style( 'ag-style', get_stylesheet_uri(), [ 'ag-fonts' ], AG_VERSION );

    // Main JS
    wp_enqueue_script( 'ag-main', AG_URI . '/assets/js/main.js', [], AG_VERSION, true );

    wp_localize_script( 'ag-main', 'agData', [
        'ajaxurl' => admin_url( 'admin-ajax.php' ),
        'nonce'   => wp_create_nonce( 'ag_contact_nonce' ),
    ] );
}
add_action( 'wp_enqueue_scripts', 'ag_enqueue_assets' );

/* ---------------------------------------------------------------
   CONTACT FORM AJAX HANDLER
--------------------------------------------------------------- */
function ag_handle_contact() {
    check_ajax_referer( 'ag_contact_nonce', 'nonce' );

    $name    = sanitize_text_field( $_POST['contact_name']  ?? '' );
    $email   = sanitize_email(      $_POST['contact_email'] ?? '' );
    $enquiry = sanitize_textarea_field( $_POST['contact_enquiry'] ?? '' );

    if ( ! $email || ! $enquiry ) {
        wp_send_json_error( [ 'message' => __( 'Please fill in all required fields.', 'amerigroup' ) ] );
    }

    $to      = get_option( 'admin_email' );
    $subject = sprintf( __( 'New Enquiry from %s — Amerigroup', 'amerigroup' ), $name ?: $email );
    $body    = "Name: {$name}\nEmail: {$email}\n\nMessage:\n{$enquiry}";
    $headers = [ "Reply-To: {$email}", 'Content-Type: text/plain; charset=UTF-8' ];

    $sent = wp_mail( $to, $subject, $body, $headers );

    if ( $sent ) {
        wp_send_json_success( [ 'message' => __( 'Thank you! We will be in touch shortly.', 'amerigroup' ) ] );
    } else {
        wp_send_json_error( [ 'message' => __( 'Sorry, there was an error. Please try again.', 'amerigroup' ) ] );
    }
}
add_action( 'wp_ajax_ag_contact',        'ag_handle_contact' );
add_action( 'wp_ajax_nopriv_ag_contact', 'ag_handle_contact' );

