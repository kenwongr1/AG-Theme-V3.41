function ag_render_team_meta( $post ) {
    wp_nonce_field( 'ag_team_meta', 'ag_team_nonce' );
    $title    = get_post_meta( $post->ID, '_ag_team_title',    true );
    $linkedin = get_post_meta( $post->ID, '_ag_team_linkedin', true );
    ?>
    <table class="form-table">
        <tr>
            <th><label for="ag_team_title"><?php esc_html_e( 'Job Title', 'amerigroup' ); ?></label></th>
            <td><input type="text" id="ag_team_title" name="ag_team_title" value="<?php echo esc_attr( $title ); ?>" class="regular-text"></td>
        </tr>
        <tr>
            <th><label for="ag_team_linkedin"><?php esc_html_e( 'LinkedIn URL', 'amerigroup' ); ?></label></th>
            <td><input type="url" id="ag_team_linkedin" name="ag_team_linkedin" value="<?php echo esc_attr( $linkedin ); ?>" class="regular-text"></td>
        </tr>
    </table>
    <?php
}

function ag_save_team_meta( $post_id ) {
    if ( ! isset( $_POST['ag_team_nonce'] ) || ! wp_verify_nonce( $_POST['ag_team_nonce'], 'ag_team_meta' ) ) return;
    if ( defined( 'DOING_AUTOSAVE' ) && DOING_AUTOSAVE ) return;
    if ( ! current_user_can( 'edit_post', $post_id ) ) return;

    if ( isset( $_POST['ag_team_title'] ) )
        update_post_meta( $post_id, '_ag_team_title', sanitize_text_field( $_POST['ag_team_title'] ) );
    if ( isset( $_POST['ag_team_linkedin'] ) )
        update_post_meta( $post_id, '_ag_team_linkedin', esc_url_raw( $_POST['ag_team_linkedin'] ) );
}
add_action( 'save_post', 'ag_save_team_meta' );

/* ---------------------------------------------------------------
   BODY CLASSES
--------------------------------------------------------------- */
function ag_body_classes( $classes ) {
    if ( is_front_page() ) $classes[] = 'is-front-page';
    if ( is_page() )       $classes[] = 'page-' . get_post_field( 'post_name', get_the_ID() );
    return $classes;
}
add_action( 'body_class', 'ag_body_classes' );

/* ---------------------------------------------------------------
   TEMPLATE REDIRECT — map page slugs to templates
--------------------------------------------------------------- */
function ag_template_redirect( $template ) {
    if ( is_page() ) {
        $slug   = get_post_field( 'post_name', get_the_ID() );
        $lookup = AG_DIR . "/page-{$slug}.php";
        if ( file_exists( $lookup ) ) return $lookup;
    }
    return $template;
}
add_filter( 'template_include', 'ag_template_redirect' );
